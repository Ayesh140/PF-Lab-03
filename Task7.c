#include <stdio.h>
int main(){
    int integer = 42;
    float floating = 3.14159;
    double doublePrecision = 2.718281828;
    char character = 'A';

    printf("Integer: %d\n", integer);
    printf("Float: %f\n", floating);
    printf("Double: %lf\n", doublePrecision);
    printf("Character: %c\n", character);

    return 0;
}